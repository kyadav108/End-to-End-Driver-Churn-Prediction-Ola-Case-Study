"""**Outlier treatment of feature Age, Income and Total Business value:**"""

# Calculate IQR for Age
Q1 = df['Age'].quantile(0.25)
Q3 = df['Age'].quantile(0.75)
IQR = Q3 - Q1

# Define bounds
lower_bound = Q1 - 1.5 * IQR
upper_bound = Q3 + 1.5 * IQR

# Cap outliers
df['Age'] = np.where(df['Age'] < lower_bound, lower_bound, df['Age'])
df['Age'] = np.where(df['Age'] > upper_bound, upper_bound, df['Age'])

# Calculate IQR for Income
Q1_income = df['Income'].quantile(0.25)
Q3_income = df['Income'].quantile(0.75)
IQR_income = Q3_income - Q1_income

# Define bounds for Income
lower_bound_income = Q1_income - 1.5 * IQR_income
upper_bound_income = Q3_income + 1.5 * IQR_income

# Cap outliers in Income
df['Income'] = np.where(df['Income'] < lower_bound_income, lower_bound_income, df['Income'])
df['Income'] = np.where(df['Income'] > upper_bound_income, upper_bound_income, df['Income'])

# Log transformation for Total Business because a large data points are outliers inputing all can lead to data loss and this feature is also highly right skewd.
df['Total Business Value'] = np.log1p(df['Total Business Value'])

# Skweness after the outlier removal
df[["Age","Income","Total Business Value"]].skew()

"""Before log transformtaion skew value was 6.97 which indiacated highly right-skewed data (long tail on the right). after log transformation data is slightly left-skewed, but much closer to symmetric.

Negative Total Business Value entries have resulted in NaN values. These can adversely affect the machine learning model, so I will drop these NaN records.
"""

# Droping Nan Value of Total Business Value
df.dropna(subset=["Total Business Value"], inplace=True)

df["Total Business Value"].isna().sum()

"""## **<font color='red'>Feature Enginnering:</font>**"""

# Extract reporting month and reporting year from MMM-YY
df['Reporting_Month'] = df['MMM-YY'].dt.to_period('M')
df['Reporting_Year'] = df['MMM-YY'].dt.to_period('Y')
df['Reporting_Month_Year'] = df['MMM-YY'].dt.to_period('M')
df['Reporting_Quarter'] = df['MMM-YY'].dt.to_period('Q')

"""Creating a new feature 'Churn' from 'LastWorkingDay': drivers who have left have a recorded last working day, while those who stayed do not."""

# Drive's Churn, 1:Churn, 0:No Churn
df['Churn'] = df['LastWorkingDate'].apply(lambda x: 0 if pd.isna(x) else 1)

# Categorize the 'Age' feature into bins such as: 'Young' (18–30), 'Middle-aged' (31–50), and 'Senior' (51+).

df['Age_Category'] = pd.cut(df['Age'], bins=[18, 30, 50, float('inf')],
                            labels=['Young', 'Middle-aged', 'Senior'])

# Driver's tenure
def tenure(X):
    if pd.isna(X['LastWorkingDate']):
        # For drivers who have not churned, calculate tenure up to the latest reporting date
        return df['MMM-YY'].max() - X['Dateofjoining']
    else:
        # For drivers who have churned, calculate tenure from joining date to last working date
        return X['LastWorkingDate'] - X['Dateofjoining']

df['Employee_Tenure'] = df.apply(tenure, axis=1)
df['Employee_Tenure_Months'] = df['Employee_Tenure'].dt.days/30

# Emloyee_Tenure_Months Vs Churn
plt.figure(figsize=(8, 6))
sns.lineplot(data=df, x=df['Reporting_Month_Year'].astype(str), y='Employee_Tenure_Months', hue='Churn')
plt.title('Employee Tenure Over Time', fontweight='bold', color='red')
plt.xlabel('Reporting Month_Year')
plt.xticks(rotation=45)
plt.ylabel('Employee Tenure (Months)')
plt.grid(True)
plt.tight_layout()

"""We can observe that drivers who have churned have a much lower tenure period compared to those who stayed. Churned drivers have a mean tenure of around 12 months, while non-churned drivers have a mean tenure of approximately 32 months."""

# Rating Change over time
